import React, { useState } from 'react'
import { dummyUserData, assets, ownerMenus } from '../../assets/assets'
import { useLocation, NavLink } from 'react-router-dom'

const Sidebar = () => {
  const user = dummyUserData
  const location = useLocation()
  const [image, setImage] = useState('')

  const updateImage = () => {
    user.image = URL.createObjectURL(image)
    setImage('')
  }

  return (
    <div className='relative min-h-screen md:flex flex-col items-center pt-8 max-w-w13 md:max-w-60 w-full border-r border-borderColor text-sm'>

      {/* Profile Image */}
      <div className='group relative'>
        <label htmlFor="image">
          <img
            src={
              image
                ? URL.createObjectURL(image)
                : user.image ||
                "https://images.unsplash.com/photo-1633332755192-727a05c4013d?q=80&w=300"
            }
            alt="profile"
            className="w-24 h-24 rounded-full object-cover"
          />

          <input
            type="file"
            id="image"
            accept="image/*"
            hidden
            onChange={(e) => setImage(e.target.files[0])}
          />

          {/* Hover Edit Icon */}
          <div className='absolute hidden top-0 right-0 left-0 bottom-0 bg-black/10 rounded-full group-hover:flex items-center justify-center cursor-pointer'>
            <img src={assets.edit_icon} alt="Edit" />
          </div>
        </label>
      </div>

      {/* Save Button */}
      {image && (
        <button
          className='absolute top-0 right-0 flex p-2 gap-1 bg-primary/10 text-primary cursor-pointer'
          onClick={updateImage}
        >
          Save <img src={assets.check_icon} width={13} alt="Save" />
        </button>
      )}

      {/* User Name */}
      <p className='mt-2 text-base max-md:hidden'>{user?.name}</p>

      {/* Navigation Menu */}
      <div className='w-full'>
        {ownerMenus.map((link) => (
          <NavLink
            key={link.path}
            to={link.path}
            className={`
              relative flex items-center gap-2 w-full py-3 pl-4 first:mt-6
              ${link.path === location.pathname ? 'bg-primary/10 text-primary' : 'text-gray-600'}
            `}
          >
            <img
              src={link.path === location.pathname ? link.coloredIcon : link.icon}
              alt={`${link.name} icon`}
            />
            <span className='max-md:hidden'>{link.name}</span>

            {link.path === location.pathname && (
              <div className='bg-primary w-1.5 h-8 rounded-r-lg absolute right-0' />
            )}
          </NavLink>
        ))}
      </div>
    </div>
  )
}

export default Sidebar
